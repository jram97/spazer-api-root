const Services = require("./../services");

class BusinessCategoryController {
    constructor() { }

    create(req, res, next) {
        Services.businessCategory.create(req.body)
            .then((result) => {
                res.status(200).json(result);
            })
            .catch((error) => {
                res.status(500).json(error);
            })
    }

    addFeature(req, res, next) {
        Services.businessCategory.addFeature(req.body)
            .then((result) => {
                res.status(200).json(result);
            })
            .catch((error) => {
                res.status(500).json(error);
            })
    }

    addService(req, res, next) {
        Services.businessCategory.addService(req.body)
            .then((result) => {
                res.status(200).json(result);
            })
            .catch((error) => {
                res.status(500).json(error);
            })
    }

    getAll(req, res, next) {
        Services.businessCategory.getAll(req.body)
            .then((result) => {
                res.status(200).json(result);
            })
            .catch((error) => {
                res.status(500).json(error);
            })
    }

    getAllActive(req, res, next) {
        Services.businessCategory.getAllActive(req.body)
            .then((result) => {
                res.status(200).json(result);
            })
            .catch((error) => {
                res.status(500).json(error);
            })
    }

    changeState(req, res, next) {
        Services.businessCategory.changeState(req.params)
            .then((result) => {
                res.status(200).json(result);
            })
            .catch((error) => {
                res.status(500).json(error);
            });
    }

    getById(req, res, next) {
        Services.businessCategory.getById(req.params)
            .then((result) => {
                res.status(200).json(result);
            })
            .catch((error) => {
                res.status(500).json(error);
            });
    }
    update(req, res, next) {
        Services.businessCategory.update(req)
            .then((result) => {
                res.status(200).json(result);
            })
            .catch((error) => {
                res.status(500).json(error);
            });
    }
}

module.exports = new BusinessCategoryController();